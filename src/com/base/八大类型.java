package com.base;

public class 八大类型 {
    public static void main(String[] args) {
//        整数
        int num1=10;//最常用
        byte num2=11;
        short num3=12;
        long num4=12L;//使用long类型需在数字后加L
//        浮点型
        float num5=1.1f;//float 后面需要加f
        double num6=111.222;
        //布尔型
        boolean num8=true;
        //字符
        char s='a';


    }
}
