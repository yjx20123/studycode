package com.base;

public class Demo3 {
    static final double PI=3.14;//常量
//变量的命名规范
    /*
    * 所有变量方法，类名：见名知意
    * 类成员变量：首字符小写和驼峰原则，除了第一个单词以外，后面的单词首字母大写
    * 局部变量：首字母小写和驼峰原则
    * 常量：大写字母和下划线
    * 类名：首字母大写和驼峰原则
    * 方法名：首字母小写和驼峰原则
    * */
    public static void main(String[] args) {
        System.out.println(PI);
    }
}
